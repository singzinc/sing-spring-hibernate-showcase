package com.singplayground.showcase.dto;

public class StockReportDto {

	private String stockNo;
	private String stockComment;
	private String stockRank;

	public String getStockNo() {
		return stockNo;
	}

	public void setStockNo(String stockNo) {
		this.stockNo = stockNo;
	}

	public String getStockComment() {
		return stockComment;
	}

	public void setStockComment(String stockComment) {
		this.stockComment = stockComment;
	}

	public String getStockRank() {
		return stockRank;
	}

	public void setStockRank(String stockRank) {
		this.stockRank = stockRank;
	}

}
